#include <stdio.h>

int	ft_str_is_uppercase(char *str)
{
	int i;
	int j;

	i = 0;
	j = 0;
	while (str[i] != 0)
	{
		if (str[i] < 'A' || str[i] > 'Z')
		{
			j++;
		}
		i++;
	}
	if (j == 0)
	{
		return (1);
	}
	else
	{
		return (0);
	}
}
